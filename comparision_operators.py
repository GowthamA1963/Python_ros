# Equal to
print("10 == 10:", 10 == 10)  # True
print("10 == 5:", 10 == 5)    # False

# Not equal to
print("10 != 5:", 10 != 5)    # True
print("10 != 10:", 10 != 10)  # False

# Greater than
print("10 > 5:", 10 > 5)      # True
print("5 > 10:", 5 > 10)      # False

# Less than
print("5 < 10:", 5 < 10)      # True
print("10 < 5:", 10 < 5)      # False


# Robot sensor threshold comparison
sensor_value = 75
threshold = 70
if sensor_value > threshold:
    print("Sensor value is above the threshold.")
else:
    print("Sensor value is below the threshold.")

